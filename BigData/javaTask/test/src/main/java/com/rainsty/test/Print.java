package com.rainsty.test;
import java.util.Arrays;

class Print {
    public static void println(Byte s) {
        System.out.println(s);
    }
    public static void println(Short s) {
        System.out.println(s);
    }
    public static void println(int i) {
        System.out.println(i);
    }
    public static void println(Long l) {
        System.out.println(l);
    }
    public static void println(char c) {
        System.out.println(c);
    }
    public static void println(Boolean b) {
        System.out.println(b);
    }
    public static void println(Float f) {
        System.out.println(f);
    }
    public static void println(Double d) {
        System.out.println(d);
    }

    public static void println(String s) {
        System.out.println(s);
    }
    public static void println(Arrays a) {
        System.out.println(a);
    }

    public static void printf(String format, Object o) {
        System.out.printf(format, o);
    }
}
